"""
经典散射核心计算模块（数值模拟方法）

物理原理：
    卢瑟福散射的经典描述基于牛顿力学和库仑定律。
    α粒子在金核的库仑场中运动，由于金核质量远大于α粒子，
    可假设金核固定在原点。

核心方程：
    库仑力: F = k * r̂ / r² (排斥力，k = Z1*Z2*α*ℏc)
    牛顿第二定律: F = d(mv)/dt
    相对论修正: p = γmv, γ = 1/√(1-v²/c²)

数值方法：
    使用欧拉方法对相对论运动方程进行数值积分，
    跟踪α粒子在库仑场中的轨迹，最终通过速度方向计算散射角。
"""
import numpy as np
from 物理参数 import m_alpha, k, hbar_c

def single_classical_scatter(b, Ek, grid_params=None):
    """
    单个粒子的经典散射数值模拟
    
    物理过程：
        1. α粒子从左侧入射（x方向），碰撞参数为b（y方向偏移量）
        2. 在库仑排斥力作用下发生偏转
        3. 通过数值积分追踪粒子轨迹
        4. 根据最终速度方向计算散射角
    
    参数：
        b: 碰撞参数 (fm) - 粒子入射方向与靶核中心的垂直距离
        Ek: α粒子动能 (MeV)
        grid_params: 可选字典，包含演化参数
    
    返回：
        theta: 散射角 (度)
    
    网格参数说明：
        x0: 初始x位置（默认-200 fm，足够远以确保处于自由运动状态）
        dt: 时间步长（默认1.0 fm/c，需满足稳定性条件）
        n_steps: 演化步数（默认8000，确保粒子离开相互作用区域）
    """
    if grid_params is None:
        grid_params = {
            'x0': -200,     # 初始x位置，远离靶核
            'dt': 1.0,      # 时间步长 (fm/c)
            'n_steps': 8000 # 演化总步数
        }
    
    x0 = grid_params['x0']
    dt = grid_params['dt']
    n_steps = grid_params['n_steps']
    
    # =============================================
    # 相对论能量-动量关系
    # =============================================
    # 总能量 E = Ek + mc² = √(p²c² + m²c⁴)
    # 在自然单位制下 (c=1): E = Ek + m, p = √(E² - m²)
    Etot = Ek + m_alpha            # 总能量 (MeV)
    p0 = np.sqrt(Etot**2 - m_alpha**2)  # 初始动量 (MeV/c)
    v0 = p0 / Etot                 # 初始速度 (v/c，自然单位制下无量纲)
    
    # =============================================
    # 初始状态设置
    # =============================================
    # 粒子从左侧沿x轴入射，y坐标为碰撞参数b
    x = x0          # 初始x位置 (fm)
    y = b           # 初始y位置 = 碰撞参数 (fm)
    vx = v0         # 初始x方向速度 (c)
    vy = 0          # 初始y方向速度 = 0
    
    # =============================================
    # 数值积分（欧拉方法）
    # =============================================
    # 离散化运动方程：
    #   dx/dt = v
    #   dv/dt = F/m (考虑相对论修正)
    # 欧拉公式：
    #   x(t+dt) = x(t) + v(t) * dt
    #   v(t+dt) = v(t) + a(t) * dt
    for step in range(n_steps):
        # 计算粒子到原点（金核）的距离
        r = np.sqrt(x**2 + y**2)
        if r < 0.1:  # 防止r=0导致数值发散
            r = 0.1
        
        # =============================================
        # 库仑力计算
        # =============================================
        # 库仑力公式: F = k * r̂ / r²
        # k = Z1*Z2*α*ℏc，在自然单位制下单位为 MeV/fm
        # 因为α粒子和金核都带正电，所以是排斥力
        Fx = k * x / r**3  # x方向力分量 (MeV/fm)
        Fy = k * y / r**3  # y方向力分量 (MeV/fm)
        
        # =============================================
        # 相对论修正的加速度计算
        # =============================================
        # 相对论运动方程: dp/dt = F
        # p = γmv, E = γmc², γ = E/mc²
        # 因此: dv/dt = F * v / p = F * c² / E
        # 在自然单位制下 (c=1): dv/dt = F / E * v * c = F * v / p
        px = vx * Etot / v0   # x方向动量 (MeV/c)
        py = vy * Etot / v0   # y方向动量 (MeV/c)
        
        ax = Fx * v0 / p0     # x方向加速度 (c/fm)
        ay = Fy * v0 / p0     # y方向加速度 (c/fm)
        
        # =============================================
        # 欧拉方法更新位置和速度
        # =============================================
        x += vx * dt
        y += vy * dt
        vx += ax * dt
        vy += ay * dt
    
    # =============================================
    # 计算散射角
    # =============================================
    # 散射角定义为入射方向（x轴正方向）与出射方向的夹角
    # 使用速度方向计算: θ = arctan(vy_final / vx_final)
    theta = np.degrees(np.arctan2(vy, vx))
    
    # 确保散射角在0-180度范围内
    if theta < 0:
        theta += 180
    
    return theta

def batch_classical_scatter(b_values, Ek, grid_params=None):
    """
    批量经典散射计算（数值模拟）
    
    参数：
        b_values: 碰撞参数数组 (fm)
        Ek: α粒子动能 (MeV)
        grid_params: 网格参数
    
    返回：
        thetas: 散射角数组 (度)
    """
    thetas = []
    for b in b_values:
        theta = single_classical_scatter(b, Ek, grid_params)
        if theta > 0:
            thetas.append(theta)
        else:
            thetas.append(np.nan)
    return np.array(thetas)