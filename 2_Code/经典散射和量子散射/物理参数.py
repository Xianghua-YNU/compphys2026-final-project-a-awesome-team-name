import numpy as np

# =============================================
# 物理常数与单位系统说明
# =============================================
# 采用自然单位制 (c = 1)，简化相对论计算
# 距离单位: fm (飞米, 10^-15 m)
# 能量单位: MeV (兆电子伏特)
# 时间单位: fm/c (飞米/光速)
# 动量单位: MeV/c (自然单位制下即 MeV)

# =============================================
# 基本物理常数
# =============================================
m_e = 0.511      # 电子静质量 (MeV/c^2)
m_alpha = 3727   # α粒子静质量 (MeV/c^2)
                 # α粒子由2个质子和2个中子组成，质量约为4倍质子质量
hbar_c = 197.3   # 约化普朗克常数 * 光速 (MeV·fm)
                 # 这个组合常数在核物理中经常出现，简化计算
alpha = 1/137.036  # 精细结构常数 (无量纲)
                   # α = e^2/(4πε₀ℏc) ≈ 1/137，描述电磁相互作用强度

# =============================================
# 散射实验参数
# =============================================
# 卢瑟福散射：α粒子轰击金箔
Z1 = 2     # α粒子电荷数 (氦原子核)
Z2 = 79    # 金核电荷数 (Au原子序数)

# =============================================
# 库仑相互作用常数
# =============================================
# 库仑力公式: F = k * q1 * q2 / r^2
# 在自然单位制下，库仑势: V(r) = Z1*Z2*α*ℏc / r = k / r
k = Z1 * Z2 * alpha * hbar_c  # MeV*fm
                              # 计算值: 2 * 79 * (1/137) * 197.3 ≈ 232 MeV·fm

# =============================================
# 实验条件参数
# =============================================
Ek_alpha = 7.7  # α粒子动能 (MeV) - 卢瑟福实验典型值
                # 对应于约7.7 MeV的α粒子轰击金箔
                # 这个能量足够高，可以进入金核的库仑场范围